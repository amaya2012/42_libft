#include <stdlib.h>

typedef struct s_list
{
	void			*content;
	struct s_list	*next;
}					t_list;

t_list	*ft_lstlast(t_list *lst)
{
	while (lst && (lst->next))
		lst = lst->next;
	return (lst);
}
